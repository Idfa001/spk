    <td><h3>SISTEM PAKAR DIAGNOSA PENYAKIT GINJAL<div align="center"></h3>
<p><strong>Sistem Pakar Diagnosa penyakit Ginjal, merupakan sistem pakar untuk membantu memberikan Informasi dini mengenali gejala-gejala penyakit pada Ginjal</strong></p>
<p style="font-family:'Verdana', Courier, monospace; color:#336; text-align:justify"><img src="images/gb1.jpg" width="350" height="280" style="float:right; " /> Penyakit ginjal adalah gangguan yang terjadi pada organ ginjal, yaitu dua buah organ berbentuk seperti kacang merah yang berada di kedua sisi tubuh bagian punggung bawah, tepatnya di bawah tulang rusuk.
Gangguan pada ginjal akan memengaruhi kinerja tubuh dalam mencuci darah yaitu menyaring limbah tubuh dan cairan berlebih yang akan menjadi urine. Secara lebih spesifik, ginjal memiliki beberapa fungsi penting, antara lain:.<br />
</br>
<p>* Menyaring limbah tubuh (termasuk zat kimia,obat-obatan, dan &nbsp;&nbsp;makanan) dalam darah.</p>
<p>* Menjaga keseimbangan kadar garam, mineral, cairan, dan &nbsp;&nbsp;asam darah dalam tubuh.</p>
<p>* Menghasilkan eritropoetin, yaitu hormon yang berfungsi dalam &nbsp;&nbsp;pembentukan sel darah merah.</p>
<p>* Menghasilkan renin, yaitu enzim yang membantu mengatur &nbsp;&nbsp;tekanan darah.</p>
<p>* Menghasilkan senyawa aktif dari vitamin D untuk menjaga kesehatan tulang.</p>
<p style="font-family:Verdana, Geneva, sans-serif; text-align:justify"><img src="images/gb2.jpg" width="350" height="230" style="float:left;" />Ketika fungsi ginjal terganggu, zat sisa limbah tubuh dan cairan yang menumpuk di dalam tubuh akan menyebabkan gejala berupa pembengkakan pada pergelangan kaki, mual, muntah, lemas, dan sesak napas.
Penyakit ginjal dapat dipicu oleh kondisi lainnya, misalnya karena diabetes dan tekanan darah tinggi. Artinya, seseorang berisiko mengalami gangguan ginjal jika mengalami diabetes, tekanan darah tinggi, atau memiliki riwayat penyakit ginjal dalam keluarga.
</br>
Penyakit ginjal dapat ditimbulkan oleh diabetes atau hipertensi. Karena itu, salah satu cara yang bisa dilakukan untuk mencegah penyakit ginjal adalah dengan mengendalikan kedua penyakit tersebut. Sebab, jika kadar gula darah dan tekanan darah tidak terkontrol, maka lama-kelamaan ginjal akan rusak. Jika mengalami gejala-gejala penyakit ginjal atau memiliki riwayat penyakit ginjal dalam keluarga, pasien disarankan untuk menjalani pemeriksaan secara rutin.ggi, atau memiliki riwayat penyakit ginjal dalam keluarga.
</p>
<p style="font-family:'Verdana', Courier, monospace; color:#336; text-align:justify">
</br>
Tergantung pada penyebab yang mendasarinya, beberapa jenis penyakit ginjal dapat diobati. Namun, sering kali, penyakit ginjal kronis tidak dapat disembuhkan. Secara umum, pengobatan terdiri dari langkah-langkah untuk membantu mengontrol gejala, mengurangi komplikasi, dan memperlambat keparahan penyakit. Jika ginjal Anda menjadi rusak parah, Anda mungkin perlu pengobatan untuk penyakit ginjal stadium akhir.
Dokter Anda akan bekerja untuk memperlambat atau mengontrol penyebab penyakit ginjal Anda. Pilihan pengobatan bervariasi, tergantung pada penyebabnya.
</p>
